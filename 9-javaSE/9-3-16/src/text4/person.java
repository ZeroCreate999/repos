package text4;

public class person {
    private String name1;
    private double age1;

    public person() {
    }

    public person(String name1, double age1) {
        this.name1 = name1;
        this.age1 = age1;
    }

    public String getName1() {
        return name1;
    }

    public void setName1(String name1) {
        this.name1 = name1;
    }

    public double getAge1() {
        return age1;
    }

    public void setAge1(double age1) {
        this.age1 = age1;
    }

}

