/* 1.创建Student类，包含如下属性
  		姓名
  		年龄
  		学历*/
public class Student {
    String name;
    int age;
    String school;

    public Student() {
    }

    public Student(String name, int age, String school) {
        this.name = name;
        this.age = age;
        this.school = school;
    }
}
